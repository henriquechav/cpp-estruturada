// C++ can represent numbers in several bases.
// cout converts automatically for decimal base
#include <iostream>
using namespace std;

int main(void)
{
    cout << "Testing number bases in C++.\n";
    cout << 25 << endl;
    cout << 0x19 << endl;
    cout << 031 << endl;
    cout << 0b11001 << endl;

    return 0;
}
